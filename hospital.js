  let currentPage = 1;
  const hospitalsPerPage = 12;
  const totalPages = Object.keys(hospitalData).length;

  function createHospitalCard(hospital) {
    return `
      <div class="col-lg-4 col-md-6 mb-4 d-flex">
        <div class="hospital-card w-100">
          <img src="${hospital.image}" alt="${hospital.name}" class="hospital-img">
          <div class="hospital-info">
            <div class="hospital-name">${hospital.name}</div>
            <div class="hospital-location">
              <i class="bi bi-geo-alt-fill"></i> ${hospital.location}
            </div>
            <div class="hospital-description">
              ${hospital.description}
            </div>
            <div class="hospital-tags">
              ${hospital.tags.map(tag => `<span class="hospital-tag">${tag}</span>`).join('')}
            </div>
            <div class="hospital-actions">
              <button class="btn btn-book">Book Appointment</button>
              <button class="btn btn-whatsapp">
                <i class="bi bi-whatsapp"></i> WhatsApp Us
              </button>
            </div>
          </div>
        </div>
      </div>
    `;
  }

  function loadHospitals(page) {
    const hospitalGrid = document.getElementById('hospitalGrid');
    hospitalGrid.innerHTML = ""; // clear old data

    const pageKey = `page${page}`;
    const hospitals = hospitalData[pageKey];

    if (hospitals) {
      hospitals.slice(0, hospitalsPerPage).forEach(hospital => {
        hospitalGrid.innerHTML += createHospitalCard(hospital);
      });
    }

    // Toggle Show More / Back button visibility
    document.getElementById('backBtn').style.display = (page > 1) ? "inline-block" : "none";
    document.getElementById('showMoreBtn').style.display = (page < totalPages) ? "inline-block" : "none";
  }

  // initial load
  loadHospitals(currentPage);

  // Show More functionality
  document.getElementById('showMoreBtn').addEventListener('click', function() {
    if (currentPage < totalPages) {
      currentPage++;
      loadHospitals(currentPage);
    }
  });

  // Back button functionality
  document.getElementById('backBtn').addEventListener('click', function() {
    if (currentPage > 1) {
      currentPage--;
      loadHospitals(currentPage);
    }
  });

  // Button actions
  document.addEventListener('click', function(e) {
    if (e.target.classList.contains('btn-book')) {
      alert('Booking appointment... (demo)');
    }
    if (e.target.classList.contains('btn-whatsapp') || e.target.closest('.btn-whatsapp')) {
      alert('Opening WhatsApp... (demo)');
    }
  });